# ============================================================================
# SonicWall API Client
# ============================================================================
class SonicWallAPI:
    """
    Unified client for the SonicOS REST API across Gen 6, 7, and 8.

    API Base Path:  https://<ip>:<port>/api/sonicos/
    Auth:           POST /api/sonicos/auth          (login)
                    DELETE /api/sonicos/auth         (logout)
    Commit:         POST /api/sonicos/config/pending (commit changes)

    The API structure is consistent across generations. Key differences:
      - Gen 6 (6.5.x):  API disabled by default; must enable in UI.
                         Some endpoints may lack newer fields.
      - Gen 7 (7.x):    API enabled by default. Full feature set.
      - Gen 8 (8.x):    API enabled by default. Same path structure as Gen 7.
                         Runs SonicOS 8.0+, some new fields for Gen 8 hardware.
    """

    # Standard headers required by SonicOS API (order matters for some firmware)
    HEADERS = OrderedDict([
        ("Accept", "application/json"),
        ("Content-Type", "application/json"),
        ("Accept-Encoding", "application/json"),
        ("Charset", "UTF-8"),
    ])

    def __init__(self, host: str, port: int, username: str, password: str,
                 label: str = "firewall", verify_ssl: bool = False):
        self.host = host
        self.port = port
        self.username = username
        self.password = password
        self.label = label
        self.verify_ssl = verify_ssl
        self.base_url = f"https://{host}:{port}/api/sonicos"
        self.session = requests.Session()
        self.session.verify = verify_ssl
        self.session.auth = (username, password)
        self.session.headers.update(self.HEADERS)
        self.authenticated = False
        self.firmware_version = None
        self.generation = None
        self.model = None

    # ------------------------------------------------------------------
    # Authentication
    # ------------------------------------------------------------------
    def login(self) -> bool:
        """Authenticate to the SonicWall API."""
        logger.info(f"[{self.label}] Logging in to {self.host}:{self.port} ...")
        try:
            resp = self.session.post(f"{self.base_url}/auth", timeout=30)
            if resp.status_code == 200:
                self.authenticated = True
                logger.info(f"[{self.label}] Login successful.")

                # Extract model from auth response if available
                try:
                    auth_data = resp.json()
                    info_list = auth_data.get("status", {}).get("info", [])
                    if info_list:
                        auth_info = info_list[0]
                        if auth_info.get("model"):
                            self.model = auth_info["model"]
                            logger.debug(f"[{self.label}] Model from auth: {self.model}")
                except Exception:
                    pass

                self._detect_firmware()

                # Enter config mode — required by SonicOS 7.x+ for write operations
                self._enter_config_mode()

                return True
            else:
                logger.error(f"[{self.label}] Login failed: HTTP {resp.status_code}")
                try:
                    logger.error(f"[{self.label}] Response: {resp.json()}")
                except Exception:
                    logger.error(f"[{self.label}] Response: {resp.text[:500]}")
                return False
        except requests.exceptions.ConnectionError as e:
            logger.error(f"[{self.label}] Connection failed: {e}")
            return False
        except Exception as e:
            logger.error(f"[{self.label}] Login error: {e}")
            return False

    def _enter_config_mode(self):
        """Enter config mode on the firewall — required for write operations on Gen 7+."""
        try:
            resp = self.session.post(f"{self.base_url}/config-mode", timeout=15)
            if resp.status_code == 200:
                logger.info(f"[{self.label}] Entered config mode.")
            else:
                # Some firmware versions may not need this or use a different path
                logger.debug(f"[{self.label}] config-mode returned HTTP {resp.status_code} (may not be required)")
        except Exception as e:
            logger.debug(f"[{self.label}] config-mode request failed: {e} (may not be required)")

    def logout(self):
        """Logout from the SonicWall API."""
        if self.authenticated:
            try:
                self.session.delete(f"{self.base_url}/auth", timeout=15)
                logger.info(f"[{self.label}] Logged out.")
            except Exception:
                pass
            self.authenticated = False

    def _detect_firmware(self):
        """Detect firmware version and generation from API endpoints."""
        # Try multiple endpoints — different SonicOS versions use different paths
        endpoints = [
            "status",
            "version",
            "administration/status",
        ]
        for ep in endpoints:
            try:
                resp = self._get(ep)
                if resp and isinstance(resp, dict):
                    # Flatten nested structures to find firmware/model keys
                    fw, model = self._find_fw_model(resp)
                    if fw:
                        self.firmware_version = str(fw)
                    if model and self.model in (None, "Unknown"):
                        self.model = str(model)
                    if self.firmware_version:
                        break
            except Exception:
                continue

        # Classify generation
        self._classify_generation()

        if self.firmware_version or self.model:
            logger.info(
                f"[{self.label}] Detected: Model={self.model}, "
                f"Firmware={self.firmware_version}, Gen={self.generation}"
            )
        else:
            logger.warning(f"[{self.label}] Could not auto-detect firmware version.")

    @staticmethod
    def _find_fw_model(data: dict, depth: int = 0) -> tuple:
        """Recursively search a response dict for firmware and model fields."""
        if depth > 3:
            return None, None
        fw = None
        model = None
        for key, val in data.items():
            key_lower = key.lower()
            if isinstance(val, str):
                if "firmware" in key_lower and "version" in key_lower:
                    fw = val
                elif key_lower == "firmware":
                    fw = val
                elif key_lower == "model" or key_lower == "product_model":
                    model = val
            elif isinstance(val, dict):
                sub_fw, sub_model = SonicWallAPI._find_fw_model(val, depth + 1)
                if sub_fw:
                    fw = sub_fw
                if sub_model:
                    model = sub_model
            elif isinstance(val, list) and val and isinstance(val[0], dict):
                sub_fw, sub_model = SonicWallAPI._find_fw_model(val[0], depth + 1)
                if sub_fw:
                    fw = sub_fw
                if sub_model:
                    model = sub_model
        return fw, model

    def _classify_generation(self):
        """Classify the generation from the firmware version string."""
        fw = self.firmware_version or ""
        if "8." in fw or fw.startswith("8"):
            self.generation = "Gen8"
        elif "7." in fw or fw.startswith("7"):
            self.generation = "Gen7"
        elif "6." in fw or fw.startswith("6"):
            self.generation = "Gen6"
        else:
            self.generation = "Unknown"

    # ------------------------------------------------------------------
    # Core HTTP helpers
    # ------------------------------------------------------------------
    def _get(self, endpoint: str, params: dict = None) -> Optional[dict]:
        """GET request to SonicOS API."""
        url = f"{self.base_url}/{endpoint}"
        try:
            resp = self.session.get(url, params=params, timeout=60)
            if resp.status_code == 200:
                return resp.json()
            elif resp.status_code == 204:
                return {}
            else:
                logger.debug(f"[{self.label}] GET {endpoint} => HTTP {resp.status_code}")
                return None
        except json.JSONDecodeError:
            return None
        except Exception as e:
            logger.debug(f"[{self.label}] GET {endpoint} error: {e}")
            return None

    def _post(self, endpoint: str, data: dict) -> tuple:
        """POST request. Returns (success: bool, response_data: dict)."""
        url = f"{self.base_url}/{endpoint}"
        try:
            resp = self.session.post(url, json=data, timeout=60)
            try:
                body = resp.json()
            except Exception:
                body = {"raw": resp.text[:500]}
            success = resp.status_code in (200, 201)
            return success, body
        except Exception as e:
            return False, {"error": str(e)}

    def _put(self, endpoint: str, data: dict) -> tuple:
        """PUT request. Returns (success: bool, response_data: dict)."""
        url = f"{self.base_url}/{endpoint}"
        try:
            resp = self.session.put(url, json=data, timeout=60)
            try:
                body = resp.json()
            except Exception:
                body = {"raw": resp.text[:500]}
            success = resp.status_code in (200, 201)
            return success, body
        except Exception as e:
            return False, {"error": str(e)}

    def commit(self) -> bool:
        """Commit pending configuration changes."""
        logger.info(f"[{self.label}] Committing pending changes ...")
        url = f"{self.base_url}/config/pending"
        try:
            resp = self.session.post(url, timeout=60)
            if resp.status_code == 200:
                logger.info(f"[{self.label}] Commit successful.")
                return True
            else:
                logger.warning(f"[{self.label}] Commit returned HTTP {resp.status_code}")
                return False
        except Exception as e:
            logger.error(f"[{self.label}] Commit error: {e}")
            return False

    # ------------------------------------------------------------------
    # Interface & VLAN discovery
    # ------------------------------------------------------------------
    def get_interfaces(self) -> list:
        """
        Retrieve all interfaces from the firewall.
        Returns a list of interface dicts with name, zone, ip, etc.
        Tries multiple API endpoints since the structure varies by generation.
        """
        interfaces = []

        # Try the standard interfaces endpoint
        for endpoint in ["interfaces/ipv4", "interfaces"]:
            resp = self._get(endpoint)
            if resp:
                # The response may nest under various keys
                items = (resp.get("interfaces", []) or
                         resp.get("interface", []))
                if isinstance(items, dict):
                    # Sometimes a single interface is returned as dict
                    items = [items]
                if isinstance(items, list) and items:
                    # Flatten nested ipv4/ipv6 wrappers if present
                    for item in items:
                        if isinstance(item, dict):
                            # Handle {"ipv4": {...}} wrapper
                            if "ipv4" in item and isinstance(item["ipv4"], dict):
                                iface = item["ipv4"]
                                iface["_ip_version"] = "ipv4"
                                interfaces.append(iface)
                            elif "ipv6" in item and isinstance(item["ipv6"], dict):
                                iface = item["ipv6"]
                                iface["_ip_version"] = "ipv6"
                                interfaces.append(iface)
                            else:
                                interfaces.append(item)
                    break  # Got results, don't try other endpoints

        if interfaces:
            logger.info(f"[{self.label}] Found {len(interfaces)} interfaces")
        else:
            logger.warning(f"[{self.label}] Could not retrieve interfaces via API")

        return interfaces

    def get_vlans(self) -> list:
        """
        Retrieve all VLAN sub-interfaces.
        VLANs appear as sub-interfaces like X0:V10, X2:V100, etc.
        """
        vlans = []
        for endpoint in ["vlans", "interfaces/vlan"]:
            resp = self._get(endpoint)
            if resp:
                items = (resp.get("vlans", []) or
                         resp.get("vlan", []) or
                         resp.get("interfaces", []))
                if isinstance(items, dict):
                    items = [items]
                if isinstance(items, list) and items:
                    vlans = items
                    break

        # If dedicated VLAN endpoint didn't work, extract VLANs from interfaces
        if not vlans:
            all_ifaces = self.get_interfaces() if not vlans else []
            for iface in all_ifaces:
                name = iface.get("name", "")
                # VLAN sub-interfaces contain ":V" or ".V" in their name
                if ":V" in name.upper() or ".V" in name.upper() or "VLAN" in name.upper():
                    vlans.append(iface)

        if vlans:
            logger.info(f"[{self.label}] Found {len(vlans)} VLAN sub-interfaces")
        else:
            logger.info(f"[{self.label}] No VLAN sub-interfaces found")

        return vlans

    def get_interface_summary(self) -> dict:
        """
        Build a structured summary of all interfaces and their VLANs.
        Returns: {
            "physical": [{"name": "X0", "zone": "LAN", "ip": "...", ...}, ...],
            "vlans": {"X0": [{"vlan_id": 10, ...}, ...], "X2": [...], ...},
            "total_physical": 8,
            "total_vlans": 5,
        }
        """
        interfaces = self.get_interfaces()
        vlans = self.get_vlans()

        # DEBUG: Dump all interfaces with duplicate names to understand Gen6 VLAN format
        name_counts = {}
        for iface in interfaces:
            n = iface.get("name", "")
            name_counts[n] = name_counts.get(n, 0) + 1
        for dup_name, count in name_counts.items():
            if count > 1:
                logger.debug(f"  [GEN6-RAW] Interface '{dup_name}' appears {count} times:")
                for idx, iface in enumerate(interfaces):
                    if iface.get("name", "") == dup_name:
                        # Truncate large fields for readability
                        display = {}
                        for k, v in iface.items():
                            if k.startswith("_"):
                                continue
                            display[k] = v
                        logger.info(f"    [{idx}] {json.dumps(display, default=str)[:500]}")

        physical = []
        vlan_map = {}  # parent_interface -> [vlan_configs]

        # Track interface names we've seen to detect duplicates
        # (Gen6 returns VLANs with same parent name repeated)
        seen_names = {}  # name -> count

        for iface in interfaces:
            name = iface.get("name", "")
            if not name:
                continue

            # Detect VLAN sub-interfaces:
            # Method 1: Name contains separator (X4:V101, X4.V101)
            is_vlan = any(sep in name for sep in [":", "."])
            # Method 2: Has vlan_tag or vlan field (Gen6 API)
            if not is_vlan:
                vlan_tag_val = (iface.get("vlan", "") or iface.get("vlan_tag", "") or
                                iface.get("vlan_id", ""))
                if vlan_tag_val and str(vlan_tag_val) not in ("", "0"):
                    is_vlan = True

            if is_vlan:
                # This is a VLAN sub-interface — handle below
                continue

            # Track for duplicate detection
            seen_names[name] = seen_names.get(name, 0) + 1

            # Only add the first occurrence as physical
            if seen_names[name] == 1:
                # Extract zone, IP, mask — handle Gen6 ip_assignment nesting
                ip = ""
                mask = ""
                zone = iface.get("zone", "")

                ip_assign = iface.get("ip_assignment", {})
                if isinstance(ip_assign, dict) and ip_assign:
                    if not zone:
                        zone = ip_assign.get("zone", "")
                    mode = ip_assign.get("mode", {})
                    if isinstance(mode, dict):
                        static = mode.get("static", {})
                        if isinstance(static, dict):
                            ip = static.get("ip", "")
                            mask = static.get("netmask", "") or static.get("mask", "")

                if not ip:
                    ip = (iface.get("ip", {}).get("ip", "") if isinstance(iface.get("ip"), dict)
                          else iface.get("ip", ""))
                if not mask:
                    mask = (iface.get("ip", {}).get("mask", "") if isinstance(iface.get("ip"), dict)
                          else iface.get("mask", ""))

                physical.append({
                    "name": name,
                    "zone": zone,
                    "ip": ip,
                    "mask": mask,
                    "management": iface.get("management", ""),
                    "comment": iface.get("comment", ""),
                    "enabled": iface.get("enabled", True),
                    "_raw": iface,
                })

        # Collect all VLAN sub-interfaces from multiple sources
        all_vlan_items = list(vlans) if vlans else []

        # Also extract VLANs from the interfaces list
        for iface in interfaces:
            name = iface.get("name", "")
            # Check for VLAN indicators
            has_separator = any(sep in name for sep in [":", "."])
            vlan_tag_val = (iface.get("vlan", "") or iface.get("vlan_tag", "") or
                            iface.get("vlan_id", ""))

            if has_separator or (vlan_tag_val and str(vlan_tag_val) not in ("", "0")):
                # Avoid duplicates (if already found via get_vlans)
                if iface not in all_vlan_items:
                    all_vlan_items.append(iface)
                    # Dump raw data for diagnostics
                    display = {k: v for k, v in iface.items() if not k.startswith("_")}
                    logger.debug(f"    [GEN6-VLAN] Found via {'name' if has_separator else 'vlan field'}: "
                                f"{json.dumps(display, default=str)}")

        # Gen6 special case: if we saw duplicate interface names (e.g., X4 
        # appearing 4 times), the extras are VLANs. Extract them by looking
        # at the second+ occurrence and checking for different zone/IP.
        for iface in interfaces:
            name = iface.get("name", "")
            if not name or any(sep in name for sep in [":", "."]):
                continue
            # If this name appeared multiple times, the extras might be VLANs
            if seen_names.get(name, 0) > 1:
                # Check if this has a different zone or IP than what we stored
                stored = next((p for p in physical if p["name"] == name), None)
                if stored and iface is not stored.get("_raw"):
                    zone = iface.get("zone", "")
                    ip_val = (iface.get("ip", {}).get("ip", "") if isinstance(iface.get("ip"), dict)
                              else iface.get("ip", ""))
                    vlan_field = iface.get("vlan", "")
                    logger.debug(f"    [VLAN-DETECT] Duplicate '{name}': "
                                f"zone={zone!r} ip={ip_val!r} vlan={vlan_field!r} "
                                f"stored_zone={stored['zone']!r} stored_ip={stored['ip']!r}")
                    logger.debug(f"    [VLAN-DETECT]   Raw keys: {sorted(iface.keys())}")
                    # Accept as VLAN if: different zone/IP OR has a vlan field
                    if (zone != stored["zone"] or ip_val != stored["ip"] or
                            (vlan_field and str(vlan_field) not in ("", "0"))):
                        if iface not in all_vlan_items:
                            all_vlan_items.append(iface)
                            logger.debug(f"    [VLAN-DETECT]   => Added as VLAN")

        # Parse VLAN items into the vlan_map structure
        for vlan in all_vlan_items:
            name = vlan.get("name", "")
            parent = ""
            vlan_tag = ""

            # Method 1: Parse from name (X4:V101 -> parent=X4, tag=101)
            for sep in [":", "."]:
                if sep in name:
                    parts = name.split(sep, 1)
                    parent = parts[0]
                    vlan_tag = parts[1].replace("V", "").replace("v", "") if len(parts) > 1 else ""
                    break

            # Method 2: Use explicit fields
            if not parent:
                parent = (vlan.get("parent_interface", "") or
                          vlan.get("parent-interface", "") or
                          vlan.get("interface", "") or
                          vlan.get("name", ""))  # Gen6: name might be the parent
                vlan_tag = str(vlan.get("vlan", "") or vlan.get("vlan_tag", "") or
                               vlan.get("vlan_id", "") or vlan.get("vlan-tag", "") or
                               vlan.get("tag", ""))

            if parent and vlan_tag:
                if parent not in vlan_map:
                    vlan_map[parent] = []

                # Extract zone, IP, and mask — handle multiple API formats:
                # Gen7/8: zone at top level, ip as {"ip": "...", "netmask": "..."}
                # Gen6:   nested in ip_assignment.zone and ip_assignment.mode.static
                zone = vlan.get("zone", "")
                ip = ""
                mask = ""

                # Check for Gen6 ip_assignment structure first
                ip_assign = vlan.get("ip_assignment", {})
                if isinstance(ip_assign, dict) and ip_assign:
                    if not zone:
                        zone = ip_assign.get("zone", "")
                    mode = ip_assign.get("mode", {})
                    if isinstance(mode, dict):
                        static = mode.get("static", {})
                        if isinstance(static, dict):
                            ip = static.get("ip", "")
                            mask = static.get("netmask", "") or static.get("mask", "")

                # Fallback: standard flat/nested ip structure
                if not ip:
                    if isinstance(vlan.get("ip"), dict):
                        ip = vlan["ip"].get("ip", "")
                        if not mask:
                            mask = (vlan["ip"].get("mask", "") or
                                    vlan["ip"].get("netmask", ""))
                    else:
                        ip = vlan.get("ip", "") or vlan.get("ip-address", "")

                if not mask:
                    mask = (vlan.get("mask", "") or vlan.get("netmask", "") or
                            vlan.get("subnet_mask", "") or vlan.get("subnet-mask", ""))

                vlan_map[parent].append({
                    "name": f"{parent}:V{vlan_tag}" if ":" not in name and "." not in name else name,
                    "vlan_tag": vlan_tag,
                    "parent": parent,
                    "zone": zone,
                    "ip": ip,
                    "mask": mask,
                    "comment": vlan.get("comment", ""),
                    "_raw": vlan,
                })

        total_vlans = sum(len(v) for v in vlan_map.values())

        # Log VLAN summary for diagnostics
        for parent, vlans_list in vlan_map.items():
            for v in vlans_list:
                logger.debug(f"    [VLAN-MAP] {v['name']}: zone={v['zone']!r} "
                            f"ip={v['ip']!r} mask={v['mask']!r} tag={v['vlan_tag']!r}")

        return {
            "physical": sorted(physical, key=lambda x: x["name"]),
            "vlans": vlan_map,
            "total_physical": len(physical),
            "total_vlans": total_vlans,
        }


