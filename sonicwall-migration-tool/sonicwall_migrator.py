    def compare_interfaces(self) -> dict:
        """
        Compare interfaces between source and target firewalls.
        Returns a dict with comparison results and compatibility info.
        """
        logger.info("=" * 70)
        logger.info("INTERFACE HARDWARE COMPARISON")
        logger.info("=" * 70)

        src_summary = self.source.get_interface_summary()
        tgt_summary = self.target.get_interface_summary()

        src_phys = src_summary["physical"]
        tgt_phys = tgt_summary["physical"]
        src_vlans = src_summary["vlans"]
        tgt_names = {i["name"] for i in tgt_phys}

        # Display source hardware
        logger.info("")
        logger.info(f"  SOURCE ({self.source.model} - {self.source.generation}):")
        logger.info(f"    Physical interfaces: {src_summary['total_physical']}")
        logger.info(f"    VLAN sub-interfaces: {src_summary['total_vlans']}")
        for iface in src_phys:
            vlan_count = len(src_vlans.get(iface["name"], []))
            vlan_str = f" ({vlan_count} VLANs)" if vlan_count else ""
            ip_str = f" [{iface['ip']}]" if iface.get("ip") else ""
            zone_str = f" Zone: {iface['zone']}" if iface.get("zone") else ""
            logger.info(f"      {iface['name']:<6}{zone_str}{ip_str}{vlan_str}")

        # Display target hardware
        logger.info("")
        logger.info(f"  TARGET ({self.target.model} - {self.target.generation}):")
        logger.info(f"    Physical interfaces: {tgt_summary['total_physical']}")
        logger.info(f"    VLAN sub-interfaces: {tgt_summary['total_vlans']}")
        for iface in tgt_phys:
            tgt_vlans_on = tgt_summary["vlans"].get(iface["name"], [])
            vlan_count = len(tgt_vlans_on)
            vlan_str = f" ({vlan_count} VLANs)" if vlan_count else ""
            ip_str = f" [{iface['ip']}]" if iface.get("ip") else ""
            zone_str = f" Zone: {iface['zone']}" if iface.get("zone") else ""
            logger.info(f"      {iface['name']:<6}{zone_str}{ip_str}{vlan_str}")

        # Check compatibility
        source_only = [i for i in src_phys if i["name"] not in tgt_names]
        has_more = len(src_phys) > len(tgt_phys)

        if source_only:
            logger.info("")
            logger.warning(
                f"  *** SOURCE has {len(source_only)} interfaces NOT available "
                f"on TARGET: {', '.join(i['name'] for i in source_only)}"
            )

        # X0 safety exclusion
        logger.info("")
        logger.info("  *** X0 is EXCLUDED from migration (safety) ***")
        logger.info("  X0 is your primary LAN/management interface and must be")
        logger.info("  configured manually when the new firewall is installed live.")

        return {
            "source": src_summary,
            "target": tgt_summary,
            "source_only_interfaces": source_only,
            "source_has_more": has_more,
            "compatible": not has_more,
        }

    def select_interfaces_interactive(self, comparison: dict) -> dict:
        """
        Interactive interface/VLAN selection with mapping.
        
        Rules:
        - X0 is always excluded (safety - must be configured manually)
        - MGMT is auto-mapped if it exists on both sides
        - When target has fewer interfaces than source, prompt for mapping
        - When target has same/more, offer 1:1 mapping selection
        - Only mapped interfaces and their VLANs will be imported
        
        Returns:
        {
            "interfaces": [list of source interface configs to migrate],
            "vlans": [list of VLAN dicts with _target_parent set],
            "mapping": {"X2": "X2", "X3": "X1", ...}
        }
        """
        EXCLUDED_INTERFACES = {"X0"}

        src_summary = comparison["source"]
        tgt_summary = comparison["target"]
        src_phys = src_summary["physical"]
        tgt_phys = tgt_summary["physical"]
        src_vlans = src_summary["vlans"]
        tgt_names = [i["name"] for i in tgt_phys]

        # Filter out X0 from source list
        src_phys_filtered = [i for i in src_phys if i["name"] not in EXCLUDED_INTERFACES]
        x0_vlans = len(src_vlans.get("X0", []))
        if x0_vlans:
            logger.info(f"    NOTE: X0 has {x0_vlans} VLANs that must be recreated manually.")

        # Filter out X0 from available targets
        tgt_names_available = [n for n in tgt_names if n not in EXCLUDED_INTERFACES]

        result = {
            "interfaces": [],
            "vlans": [],
            "mapping": {},
        }

        # --- Auto-map MGMT interface (no prompt needed) ---
        src_has_mgmt = any(i["name"] == "MGMT" for i in src_phys_filtered)
        tgt_has_mgmt = "MGMT" in tgt_names_available
        if src_has_mgmt and tgt_has_mgmt:
            mgmt_iface = next(i for i in src_phys_filtered if i["name"] == "MGMT")
            result["mapping"]["MGMT"] = "MGMT"
            result["interfaces"].append(mgmt_iface)
            tgt_names_available.remove("MGMT")
            src_phys_filtered = [i for i in src_phys_filtered if i["name"] != "MGMT"]
            logger.info("    Auto-mapped: MGMT -> MGMT")
            for vlan in src_vlans.get("MGMT", []):
                vlan["_target_parent"] = "MGMT"
                result["vlans"].append(vlan)

        if not src_phys_filtered:
            logger.info("    No additional interfaces to map (only X0/MGMT).")
            return result

        # Determine mapping approach based on interface counts
        if len(src_phys_filtered) > len(tgt_names_available):
            # Target has fewer interfaces - MUST map selectively
            print(f"\n  SOURCE has {len(src_phys_filtered)} interfaces (excl. X0/MGMT)")
            print(f"  TARGET has {len(tgt_names_available)} available interfaces")
            print("  You must map source interfaces to available target interfaces.\n")

            print("  Source interfaces:")
            for i, iface in enumerate(src_phys_filtered, 1):
                vlan_count = len(src_vlans.get(iface["name"], []))
                vlan_str = f" + {vlan_count} VLANs" if vlan_count else ""
                ip_str = f" [{iface['ip']}]" if iface.get("ip") else ""
                zone_str = f" (Zone: {iface['zone']})" if iface.get("zone") else ""
                print(f"    {i}. {iface['name']}{zone_str}{ip_str}{vlan_str}")

            print(f"\n  Available target interfaces: {', '.join(tgt_names_available)}")
            print("  Type 'done' when finished mapping.\n")

            used_targets = set()
            for src_iface in src_phys_filtered:
                src_name = src_iface["name"]
                available = [n for n in tgt_names_available if n not in used_targets]
                if not available:
                    logger.warning(f"    No more target interfaces available - skipping {src_name}")
                    continue

                choice = input(
                    f"  Map {src_name} -> [{'/'.join(available)}/skip/done]: "
                ).strip()

                if choice.lower() == "skip":
                    continue
                elif choice.lower() == "done":
                    break
                elif choice in available:
                    result["mapping"][src_name] = choice
                    result["interfaces"].append(src_iface)
                    used_targets.add(choice)
                    logger.info(f"    Mapped: {src_name} -> {choice}")
                    self._select_vlans_for_interface(src_name, choice, src_vlans, result)
                    print()
                else:
                    logger.warning(f"    Invalid target '{choice}' - skipping {src_name}")

        else:
            # Target has same or more interfaces - 1:1 mapping
            print(f"\n  Interfaces are compatible (target has enough ports, excl. X0/MGMT).")
            print("  Select which source interfaces to migrate:\n")

            for i, src_iface in enumerate(src_phys_filtered, 1):
                src_name = src_iface["name"]
                vlan_count = len(src_vlans.get(src_name, []))
                vlan_str = f" + {vlan_count} VLANs" if vlan_count else ""
                ip_str = f" [{src_iface['ip']}]" if src_iface.get("ip") else ""
                zone_str = f" (Zone: {src_iface['zone']})" if src_iface.get("zone") else ""
                print(f"    {i}. {src_name}{zone_str}{ip_str}{vlan_str}")

            selection = input(
                "\n  Enter interface numbers to migrate "
                "(comma-separated, 'all', or 'none'): "
            ).strip().lower()

            selected_ifaces = []
            if selection in ("all", "a"):
                selected_ifaces = list(range(len(src_phys_filtered)))
            elif selection not in ("none", "n", ""):
                try:
                    selected_ifaces = [int(x.strip()) - 1 for x in selection.split(",")]
                except ValueError:
                    logger.warning("Invalid selection - skipping interfaces.")

            for idx in selected_ifaces:
                if 0 <= idx < len(src_phys_filtered):
                    src_iface = src_phys_filtered[idx]
                    src_name = src_iface["name"]
                    if src_name in tgt_names_available:
                        result["mapping"][src_name] = src_name
                        result["interfaces"].append(src_iface)
                        iface_vlans = src_vlans.get(src_name, [])
                        for vlan in iface_vlans:
                            vlan["_target_parent"] = src_name
                        result["vlans"].extend(iface_vlans)
                    else:
                        logger.warning(f"    {src_name} not available on target - skipping")

        # Summary
        logger.info("")
        logger.info("  Interface mapping plan:")
        if result["interfaces"]:
            for src_name, tgt_name in result["mapping"].items():
                arrow = f"{src_name} -> {tgt_name}"
                vlan_count = len([v for v in result["vlans"]
                                  if v.get("_target_parent") == tgt_name])
                vs = f" + {vlan_count} VLANs" if vlan_count else ""
                logger.info(f"    {arrow}{vs}")
        else:
            logger.info("    No other interfaces selected for migration.")

        return result

    def _select_vlans_for_interface(self, src_name, tgt_name, src_vlans, result):
        """Helper: prompt user to select VLANs for a mapped interface."""
        iface_vlans = src_vlans.get(src_name, [])
        if not iface_vlans:
            return

        print(f"\n    VLANs on {src_name}:")
        for i, vlan in enumerate(iface_vlans, 1):
            vz = f" Zone: {vlan['zone']}" if vlan.get("zone") else ""
            vi = f" [{vlan['ip']}]" if vlan.get("ip") else ""
            vt = f" (tag {vlan['vlan_tag']})" if vlan.get("vlan_tag") else ""
            print(f"      {i}. {vlan['name']}{vt}{vz}{vi}")

        vlan_choice = input(
            f"    Migrate VLANs? [all/none/comma-separated numbers]: "
        ).strip().lower()

        if vlan_choice in ("all", "a", ""):
            for vlan in iface_vlans:
                vlan["_target_parent"] = tgt_name
            result["vlans"].extend(iface_vlans)
            logger.info(f"    Including all {len(iface_vlans)} VLANs")
        elif vlan_choice not in ("none", "n"):
            try:
                indices = [int(x.strip()) for x in vlan_choice.split(",")]
                count = 0
                for idx in indices:
                    if 1 <= idx <= len(iface_vlans):
                        vlan = iface_vlans[idx - 1]
                        vlan["_target_parent"] = tgt_name
                        result["vlans"].append(vlan)
                        count += 1
                logger.info(f"    Including {count} selected VLANs")
            except ValueError:
                logger.warning("    Invalid selection - skipping VLANs")

    def migrate_interfaces(self, interface_plan: dict):
        """
        Apply the interface migration plan to the target firewall.
        Configures interface IP settings, zones, and VLAN sub-interfaces.
        
        VLAN sub-interfaces are created using the SonicOS API format:
        POST /api/sonicos/interfaces/vlan
        {
          "interfaces": [{
            "vlan": {
              "name": "X0.V100",
              "parent-interface": "X0",
              "vlan-tag": 100,
              "zone": "LAN",
              "mode": "static",
              "ip-address": "172.16.100.1",
              "subnet-mask": "255.255.255.0",
              "comment": "VLAN 100 Sub-interface"
            }
          }]
        }
        """
        if not interface_plan["interfaces"] and not interface_plan["vlans"]:
            logger.info("  No interfaces to migrate.")
            return

        logger.info("=" * 70)
        logger.info("MIGRATING INTERFACES & VLANs")
        logger.info("=" * 70)

        mapping = interface_plan["mapping"]
        successes = 0
        errors = 0

        # --- Migrate physical interface settings ---
        for src_iface in interface_plan["interfaces"]:
            src_name = src_iface["name"]
            tgt_name = mapping.get(src_name, src_name)
            raw = src_iface.get("_raw", src_iface)

            # Clean the config for import
            cleaned = self._clean_item_for_import(raw)

            # Remap interface name if different
            if src_name != tgt_name:
                cleaned["name"] = tgt_name
                logger.info(f"    Remapping {src_name} -> {tgt_name}")

            # Build PUT body — physical interfaces already exist, use PUT
            body = {"interfaces": [{"ipv4": cleaned}]}

            item_label = (f"{src_name}->{tgt_name}" if src_name != tgt_name
                          else tgt_name)

            if self.dry_run:
                logger.info(f"    [DRY RUN] Would configure: {item_label}")
                successes += 1
                continue

            # Use PUT since interfaces already exist on the target
            ok, resp = self.target._put("interfaces/ipv4", body)
            if ok:
                successes += 1
                logger.info(f"    Configured: {item_label}")
            else:
                errors += 1
                msg = ""
                status = resp.get("status", {})
                for info in status.get("info", []):
                    msg += info.get("message", "")
                logger.warning(f"    FAILED: {item_label} — {msg}")

        # Commit interfaces before VLANs (VLANs depend on parent config)
        if successes > 0 and not self.dry_run:
            self.target.commit()
            time.sleep(2)

        # --- Migrate VLAN sub-interfaces ---
        vlan_successes = 0
        vlan_errors = 0

        for vlan in interface_plan["vlans"]:
            tgt_parent = vlan.get("_target_parent", "")
            raw = vlan.get("_raw", vlan)

            # Extract VLAN tag
            vlan_tag = vlan.get("vlan_tag", "")
            if not vlan_tag:
                name = vlan.get("name", "")
                for sep in [":V", ":v", ".V", ".v"]:
                    if sep in name:
                        try:
                            vlan_tag = int(name.split(sep, 1)[1])
                        except (ValueError, IndexError):
                            pass
                        break

            if not vlan_tag:
                vlan_errors += 1
                logger.warning(f"    FAILED VLAN: {vlan.get('name', '?')} — could not determine VLAN tag")
                continue

            vlan_tag = int(vlan_tag)

            # Extract zone, IP, and mask from the vlan dict and _raw
            # Gen6 nests data in ip_assignment.zone and ip_assignment.mode.static
            zone = vlan.get("zone", "")
            ip_addr = vlan.get("ip", "")
            subnet = vlan.get("mask", "")

            # Helper: extract from ip_assignment structure (Gen6)
            def _extract_ip_assignment(obj):
                z, i, m = "", "", ""
                ip_assign = obj.get("ip_assignment", {})
                if isinstance(ip_assign, dict) and ip_assign:
                    z = ip_assign.get("zone", "")
                    mode = ip_assign.get("mode", {})
                    if isinstance(mode, dict):
                        static = mode.get("static", {})
                        if isinstance(static, dict):
                            i = static.get("ip", "")
                            m = static.get("netmask", "") or static.get("mask", "")
                return z, i, m

            # Try vlan dict first, then _raw
            for source_obj in [vlan, raw]:
                z, i, m = _extract_ip_assignment(source_obj)
                if not zone and z:
                    zone = z
                if not ip_addr and i:
                    ip_addr = i
                if not subnet and m:
                    subnet = m

            # Fallback: standard flat/nested structures
            if not zone:
                zone = raw.get("zone", "")
            if not ip_addr:
                if isinstance(raw.get("ip"), dict):
                    ip_addr = raw["ip"].get("ip", "")
                else:
                    ip_addr = raw.get("ip-address", "") or raw.get("ip_address", "")
            if not subnet:
                if isinstance(raw.get("ip"), dict):
                    subnet = (raw["ip"].get("mask", "") or raw["ip"].get("netmask", ""))
                else:
                    subnet = (raw.get("netmask", "") or raw.get("subnet-mask", "") or
                              raw.get("mask", "") or raw.get("subnet_mask", ""))

            comment = raw.get("comment", "") or vlan.get("comment", "")
            mgmt = raw.get("management", {})

            vlan_display = f"{tgt_parent}:V{vlan_tag}"
            logger.info(f"    VLAN {vlan_display}: zone={zone!r}, ip={ip_addr!r}, mask={subnet!r}")

            if self.dry_run:
                zone_str = f" zone={zone}" if zone else ""
                ip_str = f" {ip_addr}/{subnet}" if ip_addr else ""
                logger.info(f"    [DRY RUN] Would create VLAN: {vlan_display}{zone_str}{ip_str}")
                vlan_successes += 1
                continue

            # --- SonicOS 7.x VLAN creation ---
            # Per SonicWall community & API docs:
            #   POST /api/sonicos/interfaces/ipv4  (same endpoint as physical interfaces)
            #   PUT  /api/sonicos/interfaces/ipv4/name/{PARENT}/vlan/{TAG}
            # Body uses the standard ipv4 interface schema.

            # Build the VLAN interface body in ipv4 format
            # Use the Gen6 ip_assignment structure — Gen7 accepts this for
            # physical interfaces, so it should also work for VLANs
            vlan_ipv4 = {
                "vlan": vlan_tag,
                "name": tgt_parent,
            }

            # Build ip_assignment in the Gen6 nested format
            if zone or (ip_addr and subnet):
                ip_assignment = {}
                if zone:
                    ip_assignment["zone"] = zone
                if ip_addr and subnet:
                    ip_assignment["mode"] = {
                        "static": {
                            "ip": ip_addr,
                            "netmask": subnet,
                        }
                    }
                vlan_ipv4["ip_assignment"] = ip_assignment

            if comment:
                vlan_ipv4["comment"] = comment
            if mgmt and isinstance(mgmt, dict):
                # Only include if there's at least one true value
                if any(v for v in mgmt.values() if v is True):
                    vlan_ipv4["management"] = mgmt

            # Primary format: POST interfaces/ipv4 with ipv4 wrapper
            body_primary = {"interfaces": [{"ipv4": vlan_ipv4}]}

            logger.debug(f"    VLAN POST body: {json.dumps(body_primary)}")

            ok, resp = self.target._post("interfaces/ipv4", body_primary)
            if ok:
                vlan_successes += 1
                logger.info(f"    Created VLAN: {vlan_display}")
                # After creation, configure zone/IP via PUT if we have data
                if zone or ip_addr:
                    put_ep = f"interfaces/ipv4/name/{tgt_parent}/vlan/{vlan_tag}"
                    put_ipv4 = {}
                    ip_assignment = {}
                    if zone:
                        ip_assignment["zone"] = zone
                    if ip_addr and subnet:
                        ip_assignment["mode"] = {
                            "static": {"ip": ip_addr, "netmask": subnet}
                        }
                    if ip_assignment:
                        put_ipv4["ip_assignment"] = ip_assignment
                    if comment:
                        put_ipv4["comment"] = comment
                    if mgmt and isinstance(mgmt, dict) and any(v is True for v in mgmt.values()):
                        put_ipv4["management"] = mgmt
                    put_body = {"interfaces": [{"ipv4": put_ipv4}]}
                    logger.info(f"    Configuring {vlan_display}: zone={zone}, ip={ip_addr}/{subnet}")
                    logger.debug(f"    PUT body: {json.dumps(put_body)}")
                    ok_put, resp_put = self.target._put(put_ep, put_body)
                    if ok_put:
                        logger.info(f"    Configured {vlan_display} settings")
                    else:
                        msg = ""
                        if resp_put and isinstance(resp_put, dict):
                            for info in resp_put.get("status", {}).get("info", []):
                                msg += info.get("message", "") + " "
                        logger.warning(f"    WARNING: VLAN {vlan_display} created but settings failed: {msg.strip()}")
                continue

            # Try alternative body formats
            alt_attempts = [
                # Without ipv4 wrapper
                ("interfaces/ipv4", {"interface": {"ipv4": vlan_ipv4}}),
                # With vlan sub-key format (user's original working format)
                ("interfaces/ipv4", {"interface": {
                    "parent-interface": tgt_parent,
                    "vlan-tag": vlan_tag,
                    "zone": zone or "LAN",
                    "mode": "static" if ip_addr else "",
                    "ip-address": ip_addr,
                    "subnet-mask": subnet,
                    "comment": comment,
                }}),
            ]

            created = False
            for alt_ep, alt_body in alt_attempts:
                # Remove empty string values
                if "interface" in alt_body and isinstance(alt_body["interface"], dict):
                    alt_body["interface"] = {k: v for k, v in alt_body["interface"].items() if v}
                ok2, resp2 = self.target._post(alt_ep, alt_body)
                if ok2:
                    vlan_successes += 1
                    logger.info(f"    Created VLAN: {vlan_display}")
                    created = True
                    break

            if not created:
                # Try PUT to update if it already exists
                put_ep = f"interfaces/ipv4/name/{tgt_parent}/vlan/{vlan_tag}"
                ok3, resp3 = self.target._put(put_ep, {"interface": {"ipv4": vlan_ipv4}})
                if ok3:
                    vlan_successes += 1
                    logger.info(f"    Updated VLAN: {vlan_display}")
                else:
                    vlan_errors += 1
                    msg = ""
                    for r in [resp, resp3]:
                        if r and isinstance(r, dict):
                            status = r.get("status", {})
                            for info in status.get("info", []):
                                m = info.get("message", "")
                                if m and m not in msg:
                                    msg += m + " | "
                    logger.warning(f"    FAILED VLAN: {vlan_display} — {msg.rstrip(' | ')}")

        if vlan_successes > 0 and not self.dry_run:
            self.target.commit()
            time.sleep(1)

        # Update stats
        self.stats["imported"]["Interfaces"] = successes
        self.stats["imported"]["VLANs"] = vlan_successes
        self.stats["errors"]["Interfaces"] = errors
        self.stats["errors"]["VLANs"] = vlan_errors

        logger.info(f"    Interfaces: {successes} ok, {errors} failed")
        logger.info(f"    VLANs:      {vlan_successes} ok, {vlan_errors} failed")

